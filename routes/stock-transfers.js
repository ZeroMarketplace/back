import express                  from "express";
import InputsController         from '../controllers/InputsController.js';
import StockTransfersController from '../controllers/StockTransfersController.js';
import AuthController           from '../controllers/AuthController.js';

let router = express.Router();

/**
 * @swagger
 * /api/stock-transfers:
 *   post:
 *     tags:
 *       - Stock Transfers
 *     summary: Add a Stock Transfer
 *     requestBody:
 *       required: true
 *       content:
 *         application/json:
 *           schema:
 *             type: object
 *             required:
 *               - _sourceWarehouse
 *               - _destinationWarehouse
 *               - _product
 *               - count
 *             properties:
 *               _sourceWarehouse:
 *                 type: string
 *               _destinationWarehouse:
 *                 type: string
 *               _product:
 *                 type: string
 *               count:
 *                 type: number
 *     responses:
 *       400:
 *          description: Bad Request (for validation)
 *          content:
 *              application/json:
 *                  schema:
 *                      type: object
 *                      properties:
 *                          message:
 *                              type: string
 *                          errors:
 *                              type: array
 *                              items:
 *                                  type: string
 *       403:
 *          description: Forbidden
 *       401:
 *          description: Unauthorized
 *       200:
 *         description: Successful insert
 *         content:
 *           application/json:
 *             schema:
 *               type: object
 *               properties:
 *                 _id:
 *                   type: string
 *                 _user:
 *                   type: string
 *                 _sourceWarehouse:
 *                   type: string
 *                 count:
 *                   type: number
 *                 _product:
 *                   type: string
 *                 _destinationWarehouse:
 *                   type: string
 *                 _inventoryChanges:
 *                   type: string
 *                 status:
 *                   type: string
 *                 createdAt:
 *                   type: string
 *                 updatedAt:
 *                   type: string
 *                 createdAtJalali:
 *                   type: string
 *                 updatedAtJalali:
 *                   type: string
 */
router.post(
    '/',
    AuthController.authorizeJWT,
    AuthController.checkAccess,
    function (req, res, next) {

        // create clean input
        let $input = InputsController.clearInput(req.body);

        // add author to created unit
        $input.user = req.user;

        StockTransfersController.insertOne($input).then(
            (response) => {
                return res.status(response.code).json(response.data ?? {});
            },
            (error) => {
                return res.status(error.code ?? 500).json(error.data ?? {});
            }
        );
    }
);

